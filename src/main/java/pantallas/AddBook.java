    private void btnAddBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddBookMouseClicked
        String titulo = titleField.getText();
        String autor = autorField.getText();
        String editorial = editorialField.getText();
        String edicion = edicionField.getText();
        String descripcion = descripcionField.getText();
        String categoria = categoriaField.getText();
        int cantidad = Integer.parseInt(cantidadField.getText());
        
        BooksModel libro = new BooksModel();
        libro.setTitulo(titulo);
        libro.setAutor(autor);
        libro.setEditorial(editorial);
        libro.setEdicion(edicion);
        libro.setDescripcion(descripcion);
        libro.setCategoria(categoria);
        libro.setCantidad(cantidad);
        
        try {
            DAOBooks dao = new DAOBooksImplements();
            dao.addBooks(libro);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_btnAddBookMouseClicked


